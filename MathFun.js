export const plus = (num1,num2) =>{
  const value = num1+num2
  return value
}

export const minus = (num1,num2) =>{
  const value = num1-num2
  return value
}

export const multiple = (num1,num2) =>{
  const value = num1*num2
  return value
}

export const devided = (num1,num2) =>{
  const value = num1/num2
  return value
}

